				<span style="font-family: 'Prata', serif; font-Size: 20pt;"><b>História do Taekwondo em STP</b></span>

				<p>O taekwondo surgiu em S.Tomé no ano 2002, implementado pelo Mestre Jose Moreira Miranda. O Jose Moreira Miranda mais conhecido como Miranda é um Santomense, mas viveu por muito tempo no Libreville Gabão com seus pais, no entanto os seus pais voltaram para S.Tomé e o Miranda contínuo no Libreville Gabão onde construi-o a sua vida e a sua carreira como atleta.</p>

				<p>O mesmo era um praticante do taekwondo, cinturão negro 2º-Dão e na altura já era campeão a nível de Africa Central e do Gabão no taekwondo. Entretanto veio para S.Tomé de féria em 2002 visitar seus pais. Então o Miranda aproveitou esse tempo em S.Tomé para ensinar algumas técnicas para alunos de karaté, o comité olímpico teve o conhecimento disso, e foi numa altura em que S.Tomé tinha sido convidado para participar nos jogos Africano de Abuja que seria realizado em 2003.</p>

				<p>O comité olímpico então convidou o Mestre Miranda para treinar uma equipa de atletas santomenses para participar nessa competição, o mesmo aceitou o convite e permaneceu em S.Tomé treinando uma equipa inicialmente de 12 atletas que na altura eram graduados do karaté, e foi assim que o taekwondo surgiu em S.Tomé. No mesmo ano foi criada e legalizada a Federação Nacional de Taekwondo.</p>

				<p>Após a realização dos jogos Africano de Abuja em 2003, surgiram diversas competições, e o mestre Miranda contínuo com os treinos, formando atletas de base para participar nessas competições. Daí em diante S.Tomé participou em várias competições internacionais a nível do taekwondo onde conquistou várias medalhas.</p>

				<p>Em 2007 infelizmente o Mestre Miranda deixou o país, porém os atletas já eram capazes de prosseguir com os treinos por conta própria. Então o mestre Eloy Boa Morte com ajuda de outros graduados assumiram os treinos, o continuaram a trabalhando para desenvolver o taekwondo, até os dias atuais.</p>

				<p>Atualmente o taekwondo se encontra em um nível bastante elevado, existem cerca de 5 cinturões negros, dezenas de cinturões graduado e centenas de atletas.</p>
				<p>Conta com vários clubes/academias tais como:</p>
				<ul">
					<li><b>Clube Modokwuan.</b></li>
					<li><b>Clube de Guadalupe.</b></li>
					<li><b>Clube de Bombom.</b></li>
					<li><b>Clube de Trindade.</b></li>
					<li><b>Clube de Escola Patrice Lumumba.</b></li>
				</ul>
				<p>E também é ministrada na Escola Internacional e na Escola Bambino.</p>
				
	
