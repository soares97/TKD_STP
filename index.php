			todas as artes marciais.</i></p> 
			<p class="principio"><b>4. Autocontrole:</b> <i>Deve-se chegar ao máximo de potencialidade, mantendo o controlo sobre 
			a agressividade.</i></p>
			<p class="principio"><b>5. Espírito Indomável:</b> <i>Devem orientar-se dentro dos princípios do Taekwondo e procurar que 
			estes princípios governem sua vida. Não deixar que nada os impeça em ter um espírito 
			indomável para lutar, se necessário, e estar sempre do lado da justiça e da liberdade.</i></p> 
