                <span style="font-family: 'Prata', serif; font-Size: 20pt;"><b>O que é Taekwondo.</b></span>
				
			<p>A palavra Taekwondo vem da Coréia, que em português significa “o 
			caminho dos pés e das mãos”. Existem pequenas variações quanto a 
			definição do Taekwondo, mas com certeza que em todas elas afirmam 
			que: O Taekwondo é uma arte marcial que surgiu na Coréia, e que 
			atualmente é praticada em duas vertentes, a primeira íntegra preparação 
			física, técnicas e defesa pessoal, e a segunda está voltada para competições.</p>
			
			<span style="font-family: 'Prata', serif; font-Size: 20pt;"><b>A origem do Taekwondo.</b></span>
			
			<p>Como mencionado no parágrafo acima, o Taekwondo surgiu na coreia.
			O ano do seu surgimento constitui uma discórdia entre diferentes 
			autores mas contudo estima-se que esteja na faixa dos 1500 á 2000 
			anos atrás.A Coréia antigamente era formada por 3 reinos: Koguryo,
			Baek Je e Silla. Silla era o menor dos reinos, constantemente 
			invadido e saqueado. Para se defender, o rei de Silla criou uma 
			elite de guerreiros composta por jovens aristocratas e militares, 
			sob o nome de “Hwa Rang”. Esses jovens guerreiros seguiam um rigoroso 
			código de honra, no qual o respeito e a lealdade eram suas bases.
			Os “Hwa Rangs”, juntamente com este código ficaram conhecidos por 
			“Hwa Rang-Do”. Esse grupo, além dos treinos comuns para época com o 
			uso de armas, dava ênfase também ao treinamento corpo a corpo, 
			mental e físico. Com a ajuda dos “Hwa Rang-Do”, Silla se tornou o
			reino mais forte e conseguiu a unificação dos três reinos.</p>

			<p>Muitos séculos se passaram e no dia 10 de agosto de 1910 a Coréia 
			foi invadida e dominada pelo Japão; o governo colonial japonês então 
			proibiu qualquer prática cultural coreana, não só as artes marciais 
			como também o estudo da língua coreana, o canto, as roupas tradicionais, etc.</p>

			<p>Durante o período da colonização japonesa, os coreanos praticaram de 
			forma escondida essas atividades. Com a independência da Coréia em 1945,
			vários líderes de artes marciais queriam voltar às formas tradicionais 
			coreanas de combate desarmado, e a partir de 1946, foram abandonando a
			influência do Karatê aplicada pela colonização japonesa. Tentaram então
			unificar as diferentes escolas de artes marciais, mas não obtiveram sucesso.
			O Taekwondo só ficou conhecido com esse nome no ano de 1955, quando o 
			General Choi Hong Hi conseguiu unificar as diversas escolas de “Taekwondo”,
			dando origem a International Taekwondo Federation (ITF).(Projeto e…, pag-16).</p>
			
